package StringProgram;

public class concatmethod {
    public static void main(String[]args){
        //by+operator
        String s="megha"+"chouhan";
        System.out.println(s);
       // by concat method
        String s1="megha ";
        String s2="chouhan";
        String s3=s1.concat(s2);
        System.out.println(s3);
    }
}
