package Conditional_StatementsAndScanner;

public class pyra2 {
    public static void main (String[]args){
        int i,j;
        for(i=1; i<6; i++){
            for(j=5;j>=i;j--)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}

